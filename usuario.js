class usuario {
    nombre;
    apellido;
    direccion;
    telefono;
    correo;
    nss;
    responsable;
    f_nacimiento;
    constructor(nom, ape, direc, tel, correo, nss, respo, f_nacimiento){
        this.nombre=nom;
        this.apellido=ape;
        this.direccion=direc;
        this.telefono=tel;
        this.correo=correo;
        this.nss=nss;
        this.responsable=respo;
        this.f_nacimiento=f_nacimiento;
    }
}